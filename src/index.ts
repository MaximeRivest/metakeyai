// Enhanced logging for Windows
// Save original console methods first to prevent recursion
const originalConsoleLog = console.log;
const originalConsoleError = console.error;

class WindowsLogger {
  private logFile: string;
  private debugMode: boolean;
  
  constructor() {
    this.debugMode = process.platform === 'win32';
    
    const logDir = app.isPackaged ? 
      path.join(process.env.APPDATA || '', 'MetaKeyAI', 'logs') : 
      path.join(process.cwd(), 'logs');
    
    if (!fs.existsSync(logDir)) {
      fs.mkdirSync(logDir, { recursive: true });
    }
    
    const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
    this.logFile = path.join(logDir, `metakeyai-${timestamp}.log`);
    
    // Use this.log, which now correctly uses originalConsoleLog
    this.log('ðŸš€ MetaKeyAI Debug Logger Started');
    this.log(`ðŸ“ Platform: ${process.platform} ${process.arch}`);
    this.log(`ðŸ“¦ Packaged: ${app.isPackaged}`);
    this.log(`ðŸ“‚ Log file: ${this.logFile}`);
    this.log(`ðŸ”§ Node version: ${process.version}`);
    this.log(`âš¡ Electron version: ${process.versions.electron}`);
    this.log(`ðŸ  App path: ${app.getAppPath()}`);
    this.log(`ðŸ’¾ User data: ${app.getPath('userData')}`);
    
    if (process.platform === 'win32') {
      this.log(`ðŸªŸ Windows version: ${require('os').release()}`);
      this.log(`ðŸŽ¯ Resources path: ${process.resourcesPath || 'N/A'}`);
    }
  }
  
  private writeToFile(logEntry: string) {
    try {
      fs.appendFileSync(this.logFile, logEntry);
    } catch (error) {
      originalConsoleError('Failed to write to log file:', error);
    }
  }

  log(message: string) {
    const timestamp = new Date().toISOString();
    const logEntry = `[${timestamp}] ${message}\n`;
    
    // Always log to console using original function
    originalConsoleLog(message);
    
    // Also log to file
    this.writeToFile(logEntry);
  }
  
  error(message: string, error?: any) {
    const errorMsg = error ? `${message}: ${error.message || error}` : message;
    const fullMessage = `âŒ ERROR: ${errorMsg}`;

    const timestamp = new Date().toISOString();
    const logEntry = `[${timestamp}] ${fullMessage}\n`;
    
    // Log to original console
    originalConsoleError(fullMessage);
    
    // Also log to file
    this.writeToFile(logEntry);

    if (error?.stack) {
      const stackLogEntry = `[${timestamp}] ðŸ“ Stack: ${error.stack}\n`;
      originalConsoleError(`ðŸ“ Stack: ${error.stack}`);
      this.writeToFile(stackLogEntry);
    }
    
    // For Windows, also try to show a notification
    if (this.debugMode && app.isPackaged) {
      try {
        const { Notification } = require('electron');
        if (Notification.isSupported()) {
          new Notification({
            title: 'MetaKeyAI Error',
            body: errorMsg.substring(0, 100) + (errorMsg.length > 100 ? '...' : ''),
            urgency: 'critical'
          }).show();
        }
      } catch (notifError) {
        // Ignore notification errors
      }
    }
  }
  
  debug(message: string) {
    if (this.debugMode) {
      this.log(`ðŸ› DEBUG: ${message}`);
    }
  }
  
  getLogPath(): string {
    return this.logFile;
  }
  
  getLogDirectory(): string {
    return path.dirname(this.logFile);
  }
}

